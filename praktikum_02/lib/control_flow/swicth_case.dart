void main() {
  var month = 'April';

  switch (month) {
    case 'January':
      // do something
      print('Month is January');
      break;
    case 'February':
      print('Month is February');
      break;
    case 'March':
      print('Month is March');
      break;
    case 'April':
      print('Month is April');
      break;
    case 'May':
      print('Month is May');
      break;
  }
}
