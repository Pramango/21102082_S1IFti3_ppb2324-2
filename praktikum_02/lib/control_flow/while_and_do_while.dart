void main() {
 // Menginisialisasi variabel i dengan nilai 1
 var i = 1;

 // Loop while untuk mencetak angka dari 1 hingga 100
 while (i <= 100) {
    // Mencetak nilai i
    print(i);
    // Menambahkan 1 ke i setiap iterasi
    i++;
 }

 //program do while
 void main() {
 // Menginisialisasi variabel i dengan nilai 1
 var i = 1;

 // Loop do-while untuk mencetak angka dari 1 hingga 100
 do {
    // Mencetak nilai i
    print(i);
    // Menambahkan 1 ke i setiap iterasi
    i++;
 } while (i <= 100);
}

}
